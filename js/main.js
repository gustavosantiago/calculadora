function calculateSoma() {
  const soma = 2 + 2
  document.getElementById("resultadoSoma").innerHTML = "A soma é " + soma;
}

function calculateSub() {
  const subtrair = 2 - 2
  document.getElementById("resultadoSub").innerHTML = "A subtração é " + subtrair;
}

